using System;


int month = 5;
switch (month)
{
    case 5:
        Console.WriteLine("Spring");
        break;
    case 7:
        Console.WriteLine("Summer");
        break;
    case 10:
        Console.WriteLine("Autumn");
        break;
    case 1:
        Console.WriteLine("Winter");
        break;
    default:
        Console.WriteLine("Sorry,There isn't result");
        break;
}